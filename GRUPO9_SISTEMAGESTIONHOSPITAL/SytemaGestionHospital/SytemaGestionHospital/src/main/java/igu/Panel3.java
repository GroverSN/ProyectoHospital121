    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
              String nombre = jTextField1.getText();
              String genero = jTextField2.getText();
              int edad= (Integer) jSpinner1.getValue();
              String turno = jTextField3.getText();
              String cargo = jTextField5.getText();
              int sueldo =(Integer) jSpinner2.getValue();
              
              if (nombre.isEmpty()|| genero.isEmpty()|| turno.isEmpty()||cargo.isEmpty()){
                  javax.swing.JOptionPane.showMessageDialog(this,"Todos los campos deben estar llenados");
              }else{
              PersonalDAO PersonalDAO = new PersonalDAO();
              try{
                  PersonalDAO.insertarPersonal(nombre,edad,genero,turno,cargo,sueldo);
                  javax.swing.JOptionPane.showMessageDialog(this,"Personal Registrado");
                  limpiarCampos();
              }catch (Exception e){
        javax.swing.JOptionPane.showMessageDialog(this,"no se pudo registrar al Personal");
    }}    }//GEN-LAST:event_jButton2ActionPerformed

